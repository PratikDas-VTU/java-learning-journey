package Loops;

public class ForLoop {
    public static void main(String[] args) {
        
        // Print Hello World 5 times
        for(int i = 1; i <= 5; i++){
            System.out.println("Hello World");
        }

        System.out.println();
        
        // Print numbers from 1 to 10
        for(int i = 1; i <= 10; i++){
            System.out.print(i + " ");
        }
    }
}

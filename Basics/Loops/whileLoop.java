package Loops;
public class whileLoop {
    public static void main(String[] args){
        int i = 0;
        int j = 0;
        while(i<5){
            System.out.println("Hello World!");
            i++;
        }
        do{
            System.out.println("Do While Loop!");
            j++;
        }while(j<5);
    }
}
